function legacySpecificationTarget(slug) {
  const override = legacySpecificationSectionTargets[slug];
  if (override) {
    return override;
  }

  const [chapterSlug] = slug.split('/');
  const target = legacySpecificationChapterTargets[chapterSlug];
  if (!target) {
    throw new Error(`Missing handbook redirect target for ${slug}.`);
  }
  return target;
}
